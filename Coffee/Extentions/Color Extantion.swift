        return #colorLiteral(red: 0.2658337057, green: 0.2304829359, blue: 0.1348211169, alpha: 1)
        
    }
    
    static func titleButtonColor() -> UIColor {
        return #colorLiteral(red: 0.9647058824, green: 0.8980392157, blue: 0.8196078431, alpha: 1)
        
    }
    
    static func labelTextColor() -> UIColor {
        return #colorLiteral(red: 0.5176470588, green: 0.3882352941, blue: 0.2509803922, alpha: 1)
        
    }
    
    static func mainBackgroundColor() -> UIColor {
        return #colorLiteral(red: 0.8980392157, green: 0.8980392157, blue: 0.8980392157, alpha: 1)
    }

}

import UIKit

extension UIFont {
    
    static func sfProDisplay18() -> UIFont? {
        return UIFont.init(name: "SF Pro Display Bold", size: 21)
    }
    
    static func sfProDisplay15() -> UIFont? {
        return UIFont.init(name: "SF Pro Display Regular", size: 18)
    }
}

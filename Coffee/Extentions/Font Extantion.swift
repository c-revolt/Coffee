import UIKit

extension UIFont {
    
    static func sfProDisplay18() -> UIFont? {
        return UIFont(name: "SF Pro Display", size: 18)
    }
    
    static func sfProDisplay15() -> UIFont? {
        return UIFont(name: "SF Pro Display", size: 18)
    }
}
